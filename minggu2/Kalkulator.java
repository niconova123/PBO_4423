package minggu2;

public class Kalkulator {
    public int Penjumlahan(int angka1, int angka2) {
        int total;
        total = angka1 + angka2;
        return total;

    }

    public int Pengurangan(int angka1, int angka2) {
        int total;
        total = angka1 - angka2;
        return total;
    }

    public int Perkalian(int angka1, int angka2) {
        int total;
        total = angka1 * angka2;
        return total;
    }

    public int Pembagian(int angka1, int angka2) {
        int total;
        total = angka1 / angka2;
        return total;
    }

    public int Modulus(int angka1, int angka2) {
        int total;
        total = angka1 % angka2;
        return total;
    }
}